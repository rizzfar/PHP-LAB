$y = 20;

echo "x = $x <br>";
echo "y = $y <br><br><br><br><br><br><br>";

echo "Apakah x sama dengan y? " . ($x == $y ? 'Ya' : 'Tidak') . "<br>";
echo "Apakah x tidak sama dengan y? " . ($x != $y ? 'Ya' : 'Tidak') . "<br>";
echo "Apakah x lebih besar dari y? " . ($x > $y ? 'Ya' : 'Tidak') . "<br>";
echo "Apakah x lebih kecil dari y? " . ($x < $y ? 'Ya' : 'Tidak') . "<br>";
echo "Apakah x lebih besar atau sama dengan y? " . ($x >= $y ? 'Ya' : 'Tidak') . "<br>";
echo "Apakah x lebih kecil atau sama dengan y? " . ($x <= $y ? 'Ya' : 'Tidak') . "<br>";
